    private void loadStudents() {
        List<Student> students = studentDAO.getAll();
        for (Student student : students) {
            studentComboBox.addItem(student);
        }
    }

    private void loadExams() {
        List<Exam> exams = examDAO.getAll();
        tableModel.setRowCount(0); // Clear existing rows
        for (Exam exam : exams) {
            Object[] row = {
                exam.getExamID(),
                exam.getExamName(),
                exam.getExamDate(),
                exam.getCourseID()
            };
            tableModel.addRow(row);
        }
    }

    private void assignExamToStudent() {
        Student selectedStudent = (Student) studentComboBox.getSelectedItem();
        int selectedRow = jTable1.getSelectedRow();

        if (selectedStudent == null || selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a student and an exam.");
            return;
        }

        int examID = (int) tableModel.getValueAt(selectedRow, 0);

        boolean success = studentExamDAO.create(new StudentExam(selectedStudent.getStudentID(), examID)) > 0;

        if (success) {
            JOptionPane.showMessageDialog(this, "Exam assigned successfully!");
        } else {
            JOptionPane.showMessageDialog(this, "Failed to assign exam!");
        }

    }
