    private void deleteInstructorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteInstructorActionPerformed
        // TODO add your handling code here:
        int selectedRow = instructorTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select an instructor to delete.");
            return;
        }

        // Get the selected instructor ID
        int instructorID = (int) tableModel.getValueAt(selectedRow, 0);

        // Confirm deletion
        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete Instructor ID " + instructorID + "?",
                "Confirm Deletion",
                JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            boolean success = instructorDAO.delete(instructorID);
            if (success) {
                JOptionPane.showMessageDialog(this, "Instructor deleted successfully.");
                loadInstructors();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete instructor.");
            }
        }
    }

