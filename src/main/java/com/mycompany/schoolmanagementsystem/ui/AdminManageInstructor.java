    private void loadDepartments() {
        DefaultComboBoxModel<Department> departmentModel = new DefaultComboBoxModel<>();
        departmentComboBox.setModel(departmentModel);

        List<Department> departments = departmentDAO.getAll();  // Departmanları alın
        for (Department dept : departments) {
            departmentModel.addElement(dept);  // Departmanları ekleyin
        }

    }

    private void loadInstructors() {
        List<Instructor> instructors = instructorDAO.getAll();

        // Clear any previous rows in the table
        tableModel.setRowCount(0);

        // Add rows for each instructor
        for (Instructor instructor : instructors) {
            String deptName = departmentDAO.getByID(instructor.getDepartmentID()).getDepartmentName();
            Object[] rowData = {
                instructor.getInstructorID(),
                instructor.getName(),
                instructor.getSurname(),
                deptName,
                instructor.getUsername()
            };
            tableModel.addRow(rowData);
        }
    }

    private void addInstructorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addInstructorActionPerformed
        // TODO add your handling code here:
        String name = instructortNameField.getText().trim();
        String surname = surnameField.getText().trim();
        String email = emailField.getText().trim();
        String username = instructorUsernameField.getText().trim();
        String password = passwordField.getText().trim();

        // Department seçimini alıyoruz
        Department selectedDepartment = (Department) departmentComboBox.getSelectedItem();
        String selectedGender = genderComboBox.getSelectedItem().toString();

        // Alanların kontrolü
        if (name.isEmpty() || surname.isEmpty() || email.isEmpty() || username.isEmpty() || password.isEmpty() || selectedDepartment == null) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }
        
        

        // Yeni bir Instructor nesnesi oluştur
        Instructor instructor = new Instructor();
        instructor.setName(name);
        instructor.setSurname(surname);
        instructor.setEmail(email);
        instructor.setUsername(username);
        instructor.setPassword(password);
        instructor.setDepartmentID(selectedDepartment.getDepartmentID());  // Department ID'yi alıyoruz
        instructor.setGender(selectedGender);

        
        JOptionPane.showMessageDialog(this, "Instructor added successfully!");
        loadInstructors();
        


    }//GEN-LAST:event_addInstructorActionPerformed

